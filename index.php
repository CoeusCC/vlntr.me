        	<h1>Day 1 (July 29, 2013)</h1>
            <p><span class="firstcharacter">V</span>olunteerism is one of the best things we have going for us. The desire to serve is rooted in our psyche, but can be overwhelmed by other commitments and temptations. What Vlntr.Me seeks to do is <strong>facilitate the discovery of volunteer opportunities for individuals and groups in their respective communities</strong>.</p>

            <p>There is already one <a href="http://volunteermatch.org" target="_blank" alt="Volunteer Match">website that does this well</a>, but we feel that it is targeted towards the organizations and not individuals. We aren’t seeking to compete with that model because we believe Vlntr.Me will help serve in a slightly different way; namely by focusing on the <em>communities</em> that the organizations reside in and not the organization itself.</p>

            <p>We don’t have anything to offer at this point except a promise — maybe “goal” is a better way to say it. Our goal is to <strong>help people find volunteer opportunities when and where they are free to volunteer</strong>. We want to encourage individuals and groups to participate regularly and across all portions of their community, not just on their block or in their congregation. As updates are available we will detail them here.</p>

            <p>Until we are able to better explain how we want to reach our goals, you can <a href="mailto:contact@vlntr.me">contact us</a> for more specific information and to see how you can help. But don’t wait for us to get up and running before you find a reason to go volunteer. Do it now!</p>

        <blockquote><em>&#8220;You never change things by fighting the existing reality. To change something, build a new model that makes the existing model obsolete.&#8221;</em> &mdash; Buckminster Fuller</blockquote>
